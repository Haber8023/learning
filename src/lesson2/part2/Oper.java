package lesson2.part2;


/**
 * 运算符
 */
public class Oper {
    public static void main(String[] args) {
        // 加减乘除
        int a = 5/2;
        System.out.println(a); //向下取整

        // 取余
        int b = 10%3;
//        System.out.println(b);

        // ++, --
        System.out.println(b++);
        // System.out.println(b);
        // b = b + 1;

        System.out.println(b); // b=2
        System.out.println(++b);
        // b = b + 1;
        // System.out.println(b);

        // +=, -=
        b += 1; // b = b + 1;

        // ==, >=, <=, >, <, !=

        // && 且相关， || 或相关
    }
}
